package excel.reading;

public class CoolingDetails {
	
	String coolingType;
	String coolingID;
	String type;
	double itLoad;

	
	
	public String getCoolingType() {
		return coolingType;
	}
	public void setCoolingType(String coolingType) {
		this.coolingType = coolingType;
	}
	public String getCoolingID() {
		return coolingID;
	}
	public void setCoolingID(String coolingID) {
		this.coolingID = coolingID;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public double getItLoad() {
		return itLoad;
	}
	public void setItLoad(double itLoad) {
		this.itLoad = itLoad;
	}

}
